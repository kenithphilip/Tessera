"""Per-release scanner scorecard generator.

Evaluates one or more scanners against one or more corpora at a range of
score thresholds, then writes a markdown table to disk.

CLI usage::

    python -m benchmarks.scanner_eval.report --out scanners/REPORT.md
"""

from __future__ import annotations

import argparse
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Callable

from benchmarks.scanner_eval.corpora import Corpus, load_corpus
from benchmarks.scanner_eval.metrics import (
    confusion_at_threshold,
    f1_score,
    precision,
    recall,
    roc_auc,
    tpr_at_fpr,
)

_DEFAULT_THRESHOLDS = (0.3, 0.5, 0.7)
_DEFAULT_CORPORA = ("tessera_community_v1", "lakera_gandalf", "tensor_trust")


@dataclass(frozen=True)
class ThresholdMetrics:
    """Metrics computed at a single score threshold.

    Attributes:
        threshold: Decision boundary.
        tp: True positives.
        fp: False positives.
        tn: True negatives.
        fn: False negatives.
        prec: Precision.
        rec: Recall.
        f1: F1 score.
        tpr_at_1pct_fpr: TPR interpolated at FPR=0.01.
    """

    threshold: float
    tp: int
    fp: int
    tn: int
    fn: int
    prec: float
    rec: float
    f1: float
    tpr_at_1pct_fpr: float


@dataclass(frozen=True)
class EvaluationResult:
    """Evaluation of one scanner against one corpus.

    Attributes:
        scanner_name: Human-readable scanner identifier.
        corpus_name: Corpus identifier.
        auc: AUC-ROC over the full corpus.
        n_total: Total samples evaluated.
        n_positive: Positive (injection) samples.
        n_negative: Benign samples.
        per_threshold: Metrics at each evaluated threshold.
    """

    scanner_name: str
    corpus_name: str
    auc: float
    n_total: int
    n_positive: int
    n_negative: int
    per_threshold: list[ThresholdMetrics]


def evaluate(
    scorer: Callable[[str], float],
    corpus: Corpus,
    *,
    scanner_name: str = "scanner",
    thresholds: tuple[float, ...] = _DEFAULT_THRESHOLDS,
) -> EvaluationResult:
    """Evaluate a scorer callable against a corpus at multiple thresholds.

    Args:
        scorer: Callable that takes a text string and returns a float 0.0-1.0.
            Higher means more likely malicious.
        corpus: The Corpus to evaluate against.
        scanner_name: Human-readable name for the scanner.
        thresholds: Score thresholds at which to compute P/R/F1/confusion.

    Returns:
        EvaluationResult populated with per-threshold and aggregate metrics.
    """
    texts = [p["text"] for p in corpus.payloads]
    labels = [int(p["label"]) for p in corpus.payloads]
    scores = [scorer(t) for t in texts]

    n_positive = sum(labels)
    n_negative = len(labels) - n_positive

    # AUC requires both classes present; fall back gracefully.
    if n_positive > 0 and n_negative > 0:
        auc = roc_auc(scores, labels)
    else:
        auc = float("nan")

    per_threshold: list[ThresholdMetrics] = []
    for thresh in thresholds:
        cm = confusion_at_threshold(scores, labels, thresh)
        p = precision(cm["tp"], cm["fp"])
        r = recall(cm["tp"], cm["fn"])
        f = f1_score(p, r)
        if n_positive > 0 and n_negative > 0:
            tpr_1pct = tpr_at_fpr(scores, labels, 0.01)
        else:
            tpr_1pct = float("nan")
        per_threshold.append(
            ThresholdMetrics(
                threshold=thresh,
                tp=cm["tp"],
                fp=cm["fp"],
                tn=cm["tn"],
                fn=cm["fn"],
                prec=p,
                rec=r,
                f1=f,
                tpr_at_1pct_fpr=tpr_1pct,
            )
        )

    return EvaluationResult(
        scanner_name=scanner_name,
        corpus_name=corpus.name,
        auc=auc,
        n_total=len(labels),
        n_positive=n_positive,
        n_negative=n_negative,
        per_threshold=per_threshold,
    )


def format_markdown(results: list[EvaluationResult]) -> str:
    """Render evaluation results as a markdown report.

    Produces one section per scanner with a combined per-corpus per-threshold
    table.

    Args:
        results: List of EvaluationResult objects to include in the report.

    Returns:
        Markdown string.
    """
    lines: list[str] = [
        "# Scanner Evaluation Report",
        "",
        "Generated by `python -m benchmarks.scanner_eval.report`.",
        "",
    ]

    # Group by scanner name for readability.
    scanners: dict[str, list[EvaluationResult]] = {}
    for r in results:
        scanners.setdefault(r.scanner_name, []).append(r)

    for scanner_name, scanner_results in scanners.items():
        lines += [f"## {scanner_name}", ""]
        header = (
            "| Corpus | Threshold | Precision | Recall | F1 "
            "| TP | FP | TN | FN | TPR@1%FPR | AUC-ROC |"
        )
        sep = "|---|---|---|---|---|---|---|---|---|---|---|"
        lines += [header, sep]

        for r in scanner_results:
            auc_str = f"{r.auc:.3f}" if r.auc == r.auc else "n/a"  # nan check
            for tm in r.per_threshold:
                tpr_str = (
                    f"{tm.tpr_at_1pct_fpr:.3f}"
                    if tm.tpr_at_1pct_fpr == tm.tpr_at_1pct_fpr
                    else "n/a"
                )
                lines.append(
                    f"| {r.corpus_name} | {tm.threshold:.2f} "
                    f"| {tm.prec:.3f} | {tm.rec:.3f} | {tm.f1:.3f} "
                    f"| {tm.tp} | {tm.fp} | {tm.tn} | {tm.fn} "
                    f"| {tpr_str} | {auc_str} |"
                )
        lines.append("")

    return "\n".join(lines)


def _build_results(
    corpus_names: list[str],
) -> list[EvaluationResult]:
    """Evaluate the heuristic scanner against the given corpora."""
    from tessera.scanners.heuristic import injection_score

    results: list[EvaluationResult] = []
    for name in corpus_names:
        try:
            corpus = load_corpus(name)
        except FileNotFoundError:
            print(f"Warning: corpus {name!r} not found, skipping.", file=sys.stderr)
            continue
        result = evaluate(
            injection_score,
            corpus,
            scanner_name="tessera.heuristic",
        )
        results.append(result)
    return results


def main(argv: list[str] | None = None) -> int:
    """CLI entry point.

    Args:
        argv: Argument list (defaults to sys.argv).

    Returns:
        Exit code (0 on success).
    """
    parser = argparse.ArgumentParser(
        description="Generate scanner evaluation report."
    )
    parser.add_argument(
        "--out",
        default="scanners/REPORT.md",
        help="Output path for the markdown report (default: scanners/REPORT.md).",
    )
    parser.add_argument(
        "--corpora",
        nargs="*",
        default=list(_DEFAULT_CORPORA),
        help="Corpus names to evaluate (default: all three bundled corpora).",
    )
    args = parser.parse_args(argv)

    results = _build_results(args.corpora)
    if not results:
        print("No corpora loaded; nothing to report.", file=sys.stderr)
        return 1

    md = format_markdown(results)
    out = Path(args.out)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(md)
    print(f"Report written to {out}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
